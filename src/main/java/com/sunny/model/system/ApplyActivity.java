package com.sunny.model.system;

import java.util.Date;

public class ApplyActivity {
    private Long appId;

    private String appActivity;

    private String appName;

    private String appGender;

    private String appMajor;

    private String appGrade;

    private String appPhone;

    private String appQq;

    private String appTar;

    private Date createTime;

    private Date updateTime;

    public Long getAppId() {
        return appId;
    }

    public void setAppId(Long appId) {
        this.appId = appId;
    }

    public String getAppActivity() {
        return appActivity;
    }

    public void setAppActivity(String appActivity) {
        this.appActivity = appActivity;
    }

    public String getAppName() {
        return appName;
    }

    public void setAppName(String appName) {
        this.appName = appName;
    }

    public String getAppGender() {
        return appGender;
    }

    public void setAppGender(String appGender) {
        this.appGender = appGender;
    }

    public String getAppMajor() {
        return appMajor;
    }

    public void setAppMajor(String appMajor) {
        this.appMajor = appMajor;
    }

    public String getAppGrade() {
        return appGrade;
    }

    public void setAppGrade(String appGrade) {
        this.appGrade = appGrade;
    }

    public String getAppPhone() {
        return appPhone;
    }

    public void setAppPhone(String appPhone) {
        this.appPhone = appPhone;
    }

    public String getAppQq() {
        return appQq;
    }

    public void setAppQq(String appQq) {
        this.appQq = appQq;
    }

    public String getAppTar() {
        return appTar;
    }

    public void setAppTar(String appTar) {
        this.appTar = appTar;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}